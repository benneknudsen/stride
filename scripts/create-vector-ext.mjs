import { neon } from "@neondatabase/serverless";
import { readFileSync } from "fs";
const env = readFileSync("./.env.local", "utf8");
const match = env.match(/DATABASE_URL=(.+)/);
let url = match[1].trim().replace(/^["']|["']$/g, "");
if (!url.includes("sslmode")) url += (url.includes("?") ? "&" : "?") + "sslmode=require";
const sql = neon(url);
await sql`CREATE EXTENSION IF NOT EXISTS vector;`;
const res = await sql`SELECT extname FROM pg_extension WHERE extname = 'vector';`;
console.log("vector extension:", res.length ? "OK" : "MISSING");
